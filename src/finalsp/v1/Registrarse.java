    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField3.getText().isEmpty() || jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No deje campos vacios");
        } else {
            try {
                PreparedStatement st = cn.prepareCall("Insert Into usuarios(usuario,nombre,cargo,estado,contra) values(?,?,?,?,?)");
                st.setString(1, jTextField3.getText());
                st.setString(2, jTextField1.getText());
                st.setString(3, jComboBox1.getSelectedItem().toString());
                st.setString(4, "Offline");
                char[] pass = jPasswordField1.getPassword();
                String pass1 = new String(pass);
                st.setString(5, pass1);
                int rowsInserted = st.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(rootPane, jComboBox1.getSelectedItem().toString() + " agregado");
                    Login l = new Login();
                    l.setVisible(true);
                    this.dispose();
                }
// TODO add your handling code here:
            } catch (SQLException ex) {
                Logger.getLogger(Registrarse.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

